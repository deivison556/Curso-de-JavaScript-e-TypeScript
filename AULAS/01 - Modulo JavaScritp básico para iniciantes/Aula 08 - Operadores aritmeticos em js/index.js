/* OPERADORES ARITMETICOS
   
  + / Adição
  - / Subtração
  * / Multiplicação
  ** / Potenciação
  % / Resto da divisão

*/

const num1 = 5;
const num2 = 2;
const num3 = 10;
console.log(num1 + num2 * num3)

// Operador de Incremento ++
let contador = 1;
contador++;
contador++;
contador++;
console.log(contador); //resultado sera 4

// Operador de decremento --
let segundoContador = 10;
segundoContador--;
segundoContador--;
segundoContador--;

console.log(segundoContador); // resultado sera 7
